package com.pattern.programs;

public class Solid_rectangle {

	public static void main(String[] args) {
	
		int n = 4;
		int m = 5;
		
		//outer for loop
		 for(int i=1; i<=n; i++) {
			 //inner for loop
	         for(int j=1; j<=m; j++) {
	             System.out.print("*");
	         }
	         System.out.println("");
	        
	      }
	}

}
